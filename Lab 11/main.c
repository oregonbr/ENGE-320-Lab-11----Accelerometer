static volatile uint32_t millis;

//-----------------------------------------------------------------------------
//      __   __   __  ___  __  ___      __   ___  __
//     |__) |__) /  \  |  /  \  |  \ / |__) |__  /__`
//     |    |  \ \__/  |  \__/  |   |  |    |___ .__/
//
//-----------------------------------------------------------------------------

uint32_t get_timer_ms();
void clear_millis();

//-----------------------------------------------------------------------------
//      __        __          __
//     |__) |  | |__) |    | /  `
//     |    \__/ |__) |___ | \__,
//
//-----------------------------------------------------------------------------

//=============================================================================
int main(void)
{
    

    /* Initialize the SAM system */
    SystemInit();
    SysTick_Config(48000);
    i2c_init();
	accelerometer_init();
	
    while (1) 
    {
		// if we have newbmi i2c data, update our struct
		
		if(i2c_bmiUpdated())
		{
			
			accelerometer_update();
			i2c_bmiUpdated_clear(); // keep this at end
		}
		
		if(get_timer_ms() >= 100)
		{
			
			i2c_bmi_update_accel(0x68);
			clear_millis();
		}
    }
}

//-----------------------------------------------------------------------------
//      __   __              ___  ___
//     |__) |__) | \  /  /\   |  |__
//     |    |  \ |  \/  /~~\  |  |___
//
//-----------------------------------------------------------------------------

void clear_millis()
{
	__disable_irq();
	millis = 0;
	__enable_irq();
}

uint32_t get_timer_ms()
{
	return millis;
}

//-----------------------------------------------------------------------------
//        __   __   __
//     | /__` |__) /__`
//     | .__/ |  \ .__/
//
//-----------------------------------------------------------------------------
void SysTick_Handler ()
{
	millis++;
}
